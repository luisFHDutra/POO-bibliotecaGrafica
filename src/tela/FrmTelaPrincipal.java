    private void btnConfirmarCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarCadastroActionPerformed
        if (!"".equals(tfdCadastroTitulo.getText()) && !"".equals(tfdCadastroAutor.getText())) {
            Livro livro = new Livro();

            livro.setTitulo(tfdCadastroTitulo.getText());
            livro.setAutor(tfdCadastroAutor.getText());

            livros.add(livro);

            JOptionPane.showMessageDialog(null, "Livro cadastrado");

            tfdCadastroTitulo.setText("");
            tfdCadastroAutor.setText("");

            tfdCadastroTitulo.requestFocus();
        } else {
            JOptionPane.showMessageDialog(null, "Os campos título e autor não podem estar vazios");
        }


    }//GEN-LAST:event_btnConfirmarCadastroActionPerformed

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed

        int indReserva = 0;

        boolean flag1 = false;
        boolean flag2 = false;
        boolean flag3 = false;

        boolean flag4 = false;

        if ("".equals(tfdPesquisa.getText())) {
            JOptionPane.showMessageDialog(null, "Digite algum termo à ser pesquisado");

            flag4 = true;
        }

        for (int i = 0; i < livros.size() && livros.get(i) != null; i++) {
            if (livros.get(i).getTitulo().contains(tfdPesquisa.getText()) || livros.get(i).getAutor().contains(tfdPesquisa.getText())) {
                if (flag1 == false) {
                    pnlLivro1.setVisible(true);

                    lblTitulo1.setText(livros.get(i).getTitulo());
                    lblAutor1.setText(livros.get(i).getAutor());

                    flag1 = true;

                    top4[indReserva] = livros.get(i);
                    indReserva++;
                } else if (flag2 == false) {
                    pnlLivro2.setVisible(true);

                    lblTitulo2.setText(livros.get(i).getTitulo());
                    lblAutor2.setText(livros.get(i).getAutor());

                    flag2 = true;

                    top4[indReserva] = livros.get(i);
                    indReserva++;
                } else if (flag3 == false) {
                    pnlLivro3.setVisible(true);

                    lblTitulo3.setText(livros.get(i).getTitulo());
                    lblAutor3.setText(livros.get(i).getAutor());

                    flag3 = true;

                    top4[indReserva] = livros.get(i);
                    indReserva++;
                } else {
                    pnlLivro4.setVisible(true);

                    lblTitulo4.setText(livros.get(i).getTitulo());
                    lblAutor4.setText(livros.get(i).getAutor());

                    top4[indReserva] = livros.get(i);
                }
            }
        }

        if (flag1 == false && flag4 == false) {
            JOptionPane.showMessageDialog(null, "Não foi possível encontrar o termo digitado");
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

