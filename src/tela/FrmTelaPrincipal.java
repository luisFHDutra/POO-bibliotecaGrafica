    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

        Boolean flag = false;

        for (int i = 0; i < users.size() && users.get(i) != null; i++) {
            if (tfdCodigo.getText().equals(users.get(i).getCodigo()) && String.valueOf(pwfSenha.getPassword()).equals(users.get(i).getSenha())) {
//                logado = users[i];
                logado = users.get(i);

                painelAbas.setSelectedIndex(1);
                btnMenu.setVisible(true);

                if (!tfdCodigo.getText().equals("007") && !String.valueOf(pwfSenha.getPassword()).equals("007")) {
                    btnCadastroLivros.setVisible(false);
                } else {
                    btnCadastroLivros.setVisible(true);
                }

                pnlLivro1.setVisible(false);
                pnlLivro2.setVisible(false);
                pnlLivro3.setVisible(false);
                pnlLivro4.setVisible(false);

                tfdPesquisa.setText("");
                tfdPesquisa.requestFocus();

                lblUsuarioPesquisa.setText(logado.getNome().substring(0, 1).toUpperCase() + logado.getNome().substring(1));

                flag = true;
            }
        }

        if (flag = false) {
            JOptionPane.showMessageDialog(null, "Usuário ou senha incorretos");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

