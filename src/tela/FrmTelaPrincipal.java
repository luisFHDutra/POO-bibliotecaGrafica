    private void btnConfirmaCadastroUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmaCadastroUserActionPerformed
        Boolean flag = false;
        
        if (!"".equals(tfdNomeUser.getText()) && !"".equals(tfdCodUser.getText()) && !"".equals(tfdSenhaUser.getText())) {
            for (int i = 0; i < users.size() && users.get(i) != null; i++) {
                if (!users.get(i).getCodigo().equals(tfdCodUser.getText()) && flag == false) {
                    Usuario user = new Usuario();

                    user.setNome(tfdNomeUser.getText());

                    user.setCodigo(tfdCodUser.getText());

                    user.setSenha(tfdSenhaUser.getText());

                    users.add(user);

                    tfdNomeUser.setText("");
                    tfdCodUser.setText("");
                    tfdSenhaUser.setText("");

                    JOptionPane.showMessageDialog(null, "Usuário cadastrado");
                    
                    flag = true;
                } else if (flag == false) {
                    tfdNomeUser.setText("");
                    tfdCodUser.setText("");
                    tfdSenhaUser.setText("");
                    
                    JOptionPane.showMessageDialog(null, "Código de usuário já cadastrado");
                }
            }

        } else {
            JOptionPane.showMessageDialog(null, "Nenhum dos campos pode estar vazio");
        }
    }//GEN-LAST:event_btnConfirmaCadastroUserActionPerformed

